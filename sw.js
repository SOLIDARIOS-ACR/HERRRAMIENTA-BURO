const CACHE_VERSION = "v2"; // Puedes incrementar cuando actualices la PWA
const CACHE_NAME = `herramienta-burÃ³-${CACHE_VERSION}`;

const STATIC_ASSETS = [
  "/HERRRAMIENTA-BURO/",
  "/HERRRAMIENTA-BURO/index.html",
  "/HERRRAMIENTA-BURO/icon-192.png",
  "/HERRRAMIENTA-BURO/icon-512.png"
];

// INSTALL
self.addEventListener("install", event => {
  self.skipWaiting();
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => cache.addAll(STATIC_ASSETS))
  );
});

// ACTIVATE
self.addEventListener("activate", event => {
  event.waitUntil(
    caches.keys().then(keys =>
      Promise.all(
        keys.map(key => {
          if (key !== CACHE_NAME) {
            return caches.delete(key);
          }
        })
      )
    )
  );
  self.clients.claim();
});

// FETCH - Network First
self.addEventListener("fetch", event => {
  if (event.request.method !== "GET") return;

  const url = new URL(event.request.url);
  if (url.origin !== self.location.origin) return;

  event.respondWith(
    fetch(event.request)
      .then(response => {
        const clone = response.clone();
        caches.open(CACHE_NAME).then(cache => {
          cache.put(event.request, clone);
        });
        return response;
      })
      .catch(() => caches.match(event.request))
  );

});
